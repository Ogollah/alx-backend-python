#!/usr/bin/env python3
"""
Type-annotated function for adding two floats.
"""


def add(a: float, b: float) -> float:
    """
    Function that adds two floats and returns the result.

    Parameters:
    a (float): The first float operand.
    b (float): The second float operand.

    Returns:
    float: The sum of the two input floats.
    """
    return a + b
